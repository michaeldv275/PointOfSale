     public void saveTable(){
        int total = 0;
        for(int i = 0; i < jTable2.getRowCount(); i++){  
        int Amount = Integer.parseInt(jTable2.getValueAt(i, 4)+"");
        total = Amount+total;
        amount.setText(Integer.toString(total));
        }
    }
    
    public void changeAmount(int jml){
    int total = 0;
    for(int i = 0; i < jTable2.getRowCount(); i++){  
        int Amount = Integer.parseInt(jTable2.getValueAt(i, 4)+"");
        total = Amount+total;
        int sukli= jml-total;
        change.setText(Integer.toString(sukli));
        }
    }

